import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const studioRoot = path.resolve(__dirname, '..');
const repoRoot = path.resolve(studioRoot, '..', '..');
const versionsPath = path.join(repoRoot, 'config', 'versions.json');
const outPath = path.join(studioRoot, 'src', 'generated', 'versionConfig.ts');

const versions = JSON.parse(fs.readFileSync(versionsPath, 'utf8'));

function splitSemver(value) {
  const parts = String(value).trim().split('.').map((part) => Number.parseInt(part, 10));
  if (parts.length !== 3 || parts.some((part) => Number.isNaN(part))) {
    throw new Error(`Expected semver x.y.z, got: ${value}`);
  }
  return parts;
}

function splitMinorVersion(value) {
  const parts = String(value).trim().split('.').map((part) => Number.parseInt(part, 10));
  if (parts.length !== 2 || parts.some((part) => Number.isNaN(part))) {
    throw new Error(`Expected version x.y, got: ${value}`);
  }
  return parts;
}

function formatChipMeta(chip) {
  const meta = versions.firmware[chip];
  const [fwMajor, fwMinor, fwPatch] = splitSemver(meta.firmware_version);
  const [protoMajor, protoMinor] = splitMinorVersion(meta.protocol_version);
  const [storageMajor, storageMinor] = splitMinorVersion(meta.storage_version);
  return `  ${chip}: {
    chipFamily: '${chip}',
    firmwareVersion: { major: ${fwMajor}, minor: ${fwMinor}, patch: ${fwPatch} },
    protocolFamily: '${meta.protocol_family}',
    protocolVersion: { major: ${protoMajor}, minor: ${protoMinor} },
    storageVersion: { major: ${storageMajor}, minor: ${storageMinor} },
  },`;
}

const output = [
  '/* Auto-generated by tools/studio/scripts/sync-versions.mjs. Do not edit manually. */',
  `export const PRODUCT_VERSION = ${JSON.stringify(String(versions.product.version))};`,
  '',
  'export const FIRMWARE_VERSION_META = {',
  formatChipMeta('CH592F'),
  formatChipMeta('CH552G'),
  '} as const;',
  '',
  'export type FirmwareChipFamily = keyof typeof FIRMWARE_VERSION_META;',
  '',
].join('\n');

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, output, 'utf8');
